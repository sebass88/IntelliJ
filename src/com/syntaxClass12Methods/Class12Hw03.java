package com.syntaxClass12Methods;

public class Class12Hw03 {
	
	
	
	void sayHello(String country) {
		switch(country) {
		case "USA":
			System.out.println("hello");
			break;
		case "Peru":
			System.out.println("Hola");
			break;
		case "France":
			System.out.println("Bonjour");
			break;
		case "Turkey":
			System.out.println("Mer haba");
			break;
		}
	}
	

}
