package com.syntaxClass06;

public class IncrementAndDecrementIntro {

	public static void main(String[] args) {
		
		
		int num=100;
		
		num=num+1;
		num+=1;
		System.out.println(num);
		
		num++;//increment value of variable by 1
		System.out.println(num);
		
		num--;//decrement value of varialble by 1
		
		//10++; CANNOT be used with values only with variables

	}

}
